package iie.tools;

public class NmsAlarmDeptDetail {

    private String chType;

    private Long assetTotalCount;

    private Long alarmTotalCount;

    public String getChType() {
        return chType;
    }

    public void setChType(String chType) {
        this.chType = chType;
    }

    public Long getAssetTotalCount() {
        return assetTotalCount;
    }

    public void setAssetTotalCount(Long assetTotalCount) {
        this.assetTotalCount = assetTotalCount;
    }

    public Long getAlarmTotalCount() {
        return alarmTotalCount;
    }

    public void setAlarmTotalCount(Long alarmTotalCount) {
        this.alarmTotalCount = alarmTotalCount;
    }
}
