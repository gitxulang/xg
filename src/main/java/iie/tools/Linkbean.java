package iie.tools;

public class Linkbean {

	private String id;
	private String start_node_id;
	private String start_index;
	private String end_node_id;
	private String end_index;
	private String link_color;
	private String link_type;
	private String link_dash;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLink_type() {
		return link_type;
	}
	public void setLink_type(String link_type) {
		this.link_type = link_type;
	}
	public String getLink_dash() {
		return link_dash;
	}
	public void setLink_dash(String link_dash) {
		this.link_dash = link_dash;
	}
	public String getStart_node_id() {
		return start_node_id;
	}
	public void setStart_node_id(String start_node_id) {
		this.start_node_id = start_node_id;
	}
	public String getStart_index() {
		return start_index;
	}
	public void setStart_index(String start_index) {
		this.start_index = start_index;
	}
	public String getEnd_node_id() {
		return end_node_id;
	}
	public void setEnd_node_id(String end_node_id) {
		this.end_node_id = end_node_id;
	}
	public String getEnd_index() {
		return end_index;
	}
	public void setEnd_index(String end_index) {
		this.end_index = end_index;
	}
	public String getLink_color() {
		return link_color;
	}
	public void setLink_color(String link_color) {
		this.link_color = link_color;
	}
	
	
}
